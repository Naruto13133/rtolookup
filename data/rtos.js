export const stateGroups = rtos.reduce((acc, rto) => {
  if (!acc[rto.state]) acc[rto.state] = []
  acc[rto.state].push(rto)
  return acc
}, {})

export const states = Object.keys(stateGroups).sort()

export function getRTO(code) {
  return rtos.find(r => r.code.toLowerCase() === code.toLowerCase())
}

export function getSiblings(rto) {
  return rtos.filter(r => r.state === rto.state && r.code !== rto.code).slice(0, 6)
}

export function getStateSlug(state) {
  return state.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '')
}
